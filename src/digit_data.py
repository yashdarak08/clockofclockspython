def get_digit_segments(digit_char):
    """Get rotation angles for a digit character."""
    return ROTATION.get(digit_char, [0, 0])


def is_valid_digit(digit_char):
    """Check if character is a valid digit."""
    return digit_char in DIGITS


def get_digit_pattern(digit):
    """Get the pattern for a specific digit."""
    if not is_valid_digit(digit):
        raise ValueError(f"Invalid digit: {digit}")
    return DIGITS[digit]